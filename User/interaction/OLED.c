void OLED_Write_Data(unsigned char Data)
{
    unsigned char Temp_i=8;
    OLED_DC_H;
    OLED_SCL_L;
    while(Temp_i--)
    {
        if(Data&0x80){OLED_SDA_H;}
        else{OLED_SDA_L;}
        OLED_SCL_H; 
        __nop();
        OLED_SCL_L;;    
        Data<<=1;    
    }
}

/*--------------------*OLEDÐ´ÃüÁî*--------------------*/

void OLED_Write_Command(unsigned char Cmd)
{
    unsigned char Temp_i=8;
    
    OLED_DC_L;;
    OLED_SCL_L;;
    while(Temp_i--)
    {
        if(Cmd&0x80){OLED_SDA_H;}
        else{ OLED_SDA_L;;}
        OLED_SCL_H;;
        __nop();
        OLED_SCL_L;;    
        Cmd<<=1;;   
    } 	
}

