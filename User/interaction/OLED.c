    signed char *Point_p=String;                //×ö»»ÏòÊ±ÓÃµÄÖ¸Õë
    signed char *Point_s=String;
    if(Int_Num<0)
    {
        Negative_Flag=1;
        Int_Num=0-Int_Num;
    }
    if(Int_Num==0)
        String[Num_Length++]='0';
    while(Int_Num>0)
    {
        String[Num_Length++]=Int_Num%10+'0';
        Int_Num=Int_Num/10;
    }
    if(Negative_Flag)
        String[Num_Length++]='-';
    String[Num_Length]='\0';
    Point_p=String+Num_Length-1;                //½«PÖ¸ÕëÖ¸Ïò×Ö·û´®½áÊø±êÖ¾Ö®Ç°
    
